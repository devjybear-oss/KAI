export function getCategoryDetail(categoryId: string) {
  return CATEGORY_DETAILS.find((detail) => detail.categoryId === categoryId)
}

export function getProductDetail(planId: string): ProductDetail | null {
  const stored = PRODUCT_DETAILS[planId]
  if (stored) return stored

  const plan = PLANS.find((item) => item.id === planId)
  if (!plan) return null

  return {
    planId: plan.id,
    tagline: plan.coverage,
    overview: [plan.coverage],
    highlights: [],
    related: [],
  }
}

export function getPlanById(planId: string) {
  return PLANS.find((plan) => plan.id === planId)
}
